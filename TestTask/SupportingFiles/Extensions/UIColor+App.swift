import UIKit

extension UIColor {
    static var blazeOrange: UIColor {
        return UIColor(red: 255/255, green: 105/255, blue: 44/255, alpha: 1)
    }
    
    static var blazeBlue: UIColor {
        return UIColor(red: 0, green: 122/255, blue: 255/255, alpha: 1)
    }
    
    static var blazeRed: UIColor {
        return UIColor(red: 255/255, green: 59/255, blue: 48/255, alpha: 1)
    }
    
    static var customWhite: UIColor {
        return UIColor(red: 251/255, green: 251/255, blue: 251/255, alpha: 1)
    }
    
    static var cloudyGray: UIColor {
        return UIColor(red: 160/255, green: 160/255, blue: 160/255, alpha: 1)
    }
}
